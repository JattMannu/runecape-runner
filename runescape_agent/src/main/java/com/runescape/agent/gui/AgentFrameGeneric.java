    @Override
    public void onRuneAgentEvent(RuneAgentEvent e) {
        if (e.getType().equals(RuneAgentEventTypes.TRANSFORMER_EVENT)) {
            logString(((TransformerEvent) e).toString());
        } else {
            logString(e.getType() + " ");
        }
    }

